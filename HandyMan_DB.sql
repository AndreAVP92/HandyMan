	RoleName		INT							NOT NULL,
	
	CONSTRAINT PK_IdRole  PRIMARY KEY (Id)
);
GO

IF NOT EXISTS (SELECT * FROM dbo.sysobjects WHERE id = object_id(N'ROLES_DENIED_PERMISSIONS') AND OBJECTPROPERTY(id, N'IsUserTable') = 1)
CREATE TABLE ROLES_DENIED_PERMISSIONS		
(
	IdRole_RDP			INT NOT NULL,
	IdPermission_RDP	INT NOT NULL

	CONSTRAINT PK_RoleDeniedPermission	PRIMARY KEY (IdRole_RDP, IdPermission_RDP),
	CONSTRAINT FK_IdRole_RDP			FOREIGN KEY (IdRole_RDP)		REFERENCES ROLES		(Id) ON DELETE CASCADE ON UPDATE CASCADE,
	CONSTRAINT FK_IdPermission_RDP		FOREIGN KEY (IdPermission_RDP)	REFERENCES PERMISSIONS	(Id) ON DELETE CASCADE ON UPDATE CASCADE
);
GO

