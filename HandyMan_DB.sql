IF NOT EXISTS (SELECT * FROM dbo.sysobjects WHERE id = object_id(N'USERS') AND OBJECTPROPERTY(id, N'IsUserTable') = 1)
CREATE TABLE USERS(
	Id				INT			IDENTITY(1,1)		NOT NULL,
	IdPhoto_U		INT									NULL,
	UserName		NVARCHAR(25)					NOT NULL,
	Email			NVARCHAR(50)					NOT NULL,
	Password		NVARCHAR(50)					NOT NULL,
	IdAddress_U		INT									NULL,
	LoginStatus		NVARCHAR(15)						NULL, --Conectado, Desconectado
	RegisterDate	DATE							NOT NULL, --Sólo me interesa el dia, mes y año
	IdRole_U		INT								NOT NULL,
	Status			BIT								NOT NULL

	CONSTRAINT PK_IdUser		PRIMARY KEY (Id),
	CONSTRAINT FK_IdPhoto_U		FOREIGN KEY (IdPhoto_U)	  REFERENCES PHOTOS		(Id) ON DELETE CASCADE ON UPDATE CASCADE, 
	CONSTRAINT FK_IdRole_U		FOREIGN KEY (IdRole_U)	  REFERENCES ROLES		(Id) ON DELETE CASCADE ON UPDATE CASCADE,
	CONSTRAINT FK_IdAddress_U	FOREIGN KEY (IdAddress_U) REFERENCES ADDRESSES	(Id) ON DELETE CASCADE ON UPDATE CASCADE
);
GO

IF NOT EXISTS (SELECT * FROM dbo.sysobjects WHERE id = object_id(N'USERS_x_SCORES') AND OBJECTPROPERTY(id, N'IsUserTable') = 1)
